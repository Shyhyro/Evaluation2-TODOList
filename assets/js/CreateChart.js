import Chart from 'chart.js/auto'
import {getRelativePosition} from "chart.js/helpers";

let ctx = document.getElementById('myChart');

class createChart {
    create = () => {
        new Chart(ctx, {
            type: "line",
            data: {
                labels: ['Delete'],
                datasets: [{
                    label: '# of delete',
                    data: [parseInt(localStorage.getItem('delete'))],
                    backgroundColor: [
                        'rgba(255, 99, 132, 0.2)',
                        'rgba(54, 162, 235, 0.2)',
                        'rgba(255, 206, 86, 0.2)',
                        'rgba(75, 192, 192, 0.2)',
                        'rgba(153, 102, 255, 0.2)',
                        'rgba(255, 159, 64, 0.2)'
                    ],
                    borderColor: [
                        'rgba(255, 99, 132, 1)',
                        'rgba(54, 162, 235, 1)',
                        'rgba(255, 206, 86, 1)',
                        'rgba(75, 192, 192, 1)',
                        'rgba(153, 102, 255, 1)',
                        'rgba(255, 159, 64, 1)'
                    ],
                    borderWidth: 1
                }]
            },
            options: {
                scales: {
                    y: {
                        beginAtZero: true
                    }
                }
            }
        })
    }
}

export {createChart};